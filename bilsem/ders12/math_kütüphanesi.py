import math
print(math.factorial(5)) #fonksiyon alır
print(math.ceil(25.03)) #bir üste yuvarlar
print(math.floor(25.99)) #bir alta yuvarlar
print(math.gcd(6,3,9)) #ebob verir
print(math.pow(2,5)) #2'nin 5.kuvvetini hesaplar
print(math.sqrt(25)) #karekökünü alır
print(math.fabs(-25)) #mutlak değer alır
print(math.pi)
